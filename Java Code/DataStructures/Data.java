package DataStructures;

public class Data {
	int x;
	int y;
	float value;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public float getValue() {
		return value;
	}

	public void setValue(float value) {
		this.value = value;
	}

	public Data(int x, int y, float value) {
		this.x = x;
		this.y = y;
		this.value = value;

	}

}
